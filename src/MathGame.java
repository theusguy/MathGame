    private void AnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnswerActionPerformed

        //calculate

        char checkOperator = sign.getText().charAt(0);
        System.out.println(checkOperator);
        n1 = Integer.parseInt(num1.getText());
        n2 = Integer.parseInt(num2.getText());

        double resultDouble = 0.0;

        if(answerTest.getText().equals("")){

            JOptionPane.showMessageDialog(null, "Must input answer!!!");
        }
        else{
            int userAnswer = Integer.parseInt(answerTest.getText());
            switch(checkOperator){

                case '-':
                result =subtration(n1, n2);

                if(result == userAnswer){

                    user.CorrectAns();

                }
                else{
                    user.IncorrectAns();
                }
                updateFields();
                break;

                case '+':
                result =addition(n1, n2);
                System.out.println("R: " + result + " U : " + userAnswer);
                if(result == userAnswer){

                    user.CorrectAns();

                }
                else{
                    user.IncorrectAns();
                }
                updateFields();
                break;

                case '*':
                result =multiplication(n1, n2);

                if(result == userAnswer){

                    user.CorrectAns();

                }
                else{
                    user.IncorrectAns();
                }
                updateFields();
                break;

                case '/':
                resultDouble =division(n1, n2);
                double userAnswerDouble = Double.parseDouble(answerTest.getText());
                if(resultDouble == userAnswerDouble){

                    user.CorrectAns();

                }
                else{
                    user.IncorrectAns();
                }

                break;

            }
            updateFields();
            answerTest.setText("");
            generateQuestion();
        }

    }//GEN-LAST:event_AnswerActionPerformed

