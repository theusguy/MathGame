      public static void insertRecords() throws FileNotFoundException{
          
          File file = new File("score.txt");
          //BufferedReader br = new BufferedReader(new FileReader(file));
          PrintWriter pw = new PrintWriter(file);
          
          for(GameRecord list : userList){
              
              pw.write(list.toString()+"\n");
          }
          pw.close();
          
          
      }
      
