          public static void generateQuestion(){
         Random random = new Random();
        n1 = rq.getNum1();
        n2 = rq.getNum2();
        
        operator= rq.getOperator();
        if(operator == '-' && n1 < n2){
            
           
            num1.setText(Integer.toString(n2));
            num2.setText(Integer.toString(n1));
        }
        else{
            
            num1.setText(Integer.toString(n1));
            num2.setText(Integer.toString(n2));
        }
        
        sign.setText(Character.toString(operator));
         
     }
          
           public static void updateFields(){
        
 
        questions.setText(Integer.toString(user.getQuestions()));
        
     
        
        if(user.getQuestions() == 0){
            
            gameOver();
            int dialogResult = JOptionPane.showConfirmDialog (null, "Would You Like to Play again ?");
            if(dialogResult == JOptionPane.YES_OPTION){
                 userList.add(user);
                try {
                    insertRecords();
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(MathGame.class.getName()).log(Level.SEVERE, null, ex);
                }
                  loadGameRecords();
                   restart();
                  
                  
                  
                  
              }
            else{
                System.exit(0);
            }
        }
        
        
        score.setText(Integer.toString(user.getScore()));
        
        
    }
